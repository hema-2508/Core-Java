public class PositiveNegative {
    public static void main(String[] args) {
        int num=0;
        if(num>0){
            System.out.println("number is positive");
        }
        else if(num==0){
            System.out.println("num is zero");
        }
        else {
            System.out.println("num is negative");
        }
    }
}
